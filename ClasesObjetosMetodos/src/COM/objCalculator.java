package COM;

public class objCalculator {


    public double getSum(double a, double b)
    {
        return a + b;
    }

    public double getDif(double a, double b)
    {
        return a - b;
    }

    public double getMul(double a, double b)
    {
        return a * b;
    }

    public double getDiv(double a, double b)
    {
        return a / b;
    }


}
